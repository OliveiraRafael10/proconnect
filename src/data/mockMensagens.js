// Função para buscar conversas por ID
export const buscarConversaPorId = (id) => {
  return mensagensMock.find(conversa => conversa.id === id);
};

// Função para buscar conversas por nome
export const buscarConversaPorNome = (nome) => {
  return mensagensMock.filter(conversa => 
    conversa.nome.toLowerCase().includes(nome.toLowerCase())
  );
};

// Função para adicionar nova mensagem a uma conversa
export const adicionarMensagem = (conversaId, novaMensagem) => {
  const conversa = buscarConversaPorId(conversaId);
  if (conversa) {
    conversa.mensagens.push(novaMensagem);
    return true;
  }
  return false;
};

// Função para obter conversas ordenadas por última mensagem
export const obterConversasOrdenadas = () => {
  return mensagensMock.sort((a, b) => {
    const ultimaMsgA = a.mensagens[a.mensagens.length - 1];
    const ultimaMsgB = b.mensagens[b.mensagens.length - 1];
    return new Date(ultimaMsgB.dataHora) - new Date(ultimaMsgA.dataHora);
  });
};
